public class BackupFolderInfo {
    private String id;
    private String foldername;

    public void setId(String id) {
        this.id = id;
    }

    public void setFoldername(String foldername) {
        this.foldername = foldername;
    }

    public String getId() {
        return id;
    }

    public String getFoldername() {
        return foldername;
    }
}
